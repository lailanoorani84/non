            <h3 style="text-align:center;color:#2E4372;"><u>About Phishing (Potential Security Threats)</u></h3>
              
            <div class="contact" style="overflow: scroll;">
            'Phishing' is a common form of Internet piracy. It is deployed to steal users personal and confidential information like bank account numbers, net banking passwords, credit card numbers, personal identity details etc. Later the perpetrators may use the information for siphoning money from the victim's account or run up bills on victim's credit cards. In the worst case one could also become the victim of identity theft. A few customers of some other Indian banks have been affected by the attempt of phishing in early 2006.

We would like you to be aware of methodologies in a 'Phishing' attack, do's and don'ts in sharing of personal information and the action to be taken in case you fall prey to a phishing attempt.

<h4>Methodologies:</h4>

<li>
Phishing attacks use both social engineering and technical subterfuge to steal customers' personal identity data and financial account credentials.</li>
<li>Customer receives a fraudulent e-mail seemingly from a legitimate Internet address.</li>
<li>The email invites the customer to click on a hyperlink provided in the mail.</li>
<li>Click on the hyperlink directs the customer to a fake web site that looks similar to the genuine site.</li>
<li>Usually the email will either promise a reward on compliance or warn of an impending penalty on a non compliance.</li>
<li>Customer is asked to update his personal information, such as passwords and credit card and bank account numbers etc.</li>
<li>Customer provides personal details in good faith. Clicks on 'submit' button.</li>
<li>He gets an error page.</li>
<li>Customer falls prey to the phishing attempt.
</li>


<h4>Dont's:</h4>

<li>Do not click on any link which has come through e-mail from an unexpected source. It may contain malicious code or could be an attempt to 'Phish'.</li>
<li>Do not provide any information on a page which might have come up as a pop-up window.</li>
<li>Never provide your password over the phone or in response to an unsolicited request over e-mail.</li>
<li>Always remember that information like password, PIN, TIN, etc are strictly confidential and are not known even to employees/service personnel of the Bank. You should therefore, never divulge such information even if asked for.</li>


<h4>Do's:</h4>

<li>Always logon to a site by typing the proper URL in the address bar.</li>
<li>Give your user id and password only at the authenticated login page.</li>
<li>Before providing your user id and password please ensure that the URL of the login page starts with the text ‘https://’ and is not ‘http:// ‘.The 's' stands for 'secured' and indicates that the Web page uses encryption.</li>
<li>Please also look for the lock sign (lock icon) at the right bottom of the browser and the verisign certificate.</li>
<li>Provide your personal details over phone/Internet only if you have initiated a call or session and the counterpart has been duly authenticated by you.</li>
<li>Please remember that the bank would never ask you to verify your account information through an e-mail.</li>




